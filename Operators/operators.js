// JavaScript Arithmetic Operators

// Addition[+] Subtraction[-] Multiplication[*] Division[/]
// Exponentiation[**] Increment[++] Decrement[--] Modulus[%]

var a = 5;
var b = (15 + 5) * a;
console.log(b);

var x = 5;
x++;
var z = x;
console.log(x);



