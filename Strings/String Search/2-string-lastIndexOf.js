// JavaScript String lastIndexOf()

// The lastIndexOf() method returns the index of the
// last occurrence of a specified text in a string.

// Both indexOf(), and lastIndexOf() return -1 if the text is not found.

// Both methods accept a second parameter as the starting position for the search.

var text = "JavaScript String IndexOf method returns the index!";

var index = text.lastIndexOf("returns");

console.log(index); // Output 33