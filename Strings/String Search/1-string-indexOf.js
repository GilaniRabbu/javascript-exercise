







// JavaScript String indexOf()

// The indexOf() method returns the index (position)
// the first occurrence of a string in a string

// JavaScript counts positions from zero.

// 0 is the first position in a string, 1 is the second, 2 is the third, ...

// Both indexOf(), and lastIndexOf() return -1 if the text is not found

// Both methods accept a second parameter as the starting position for the search

var text = "JavaScript String IndexOf method returns the index!";

var index = text.indexOf("method");

console.log(index); // Output 26