// JavaScript String substr()

// substr() is similar to slice().

// substring() Syntax - substr(start, length)

// The difference is that the second parameter specifies the length of the extracted part.



// ========================================



const messages = "JavaScript String substr";

let res = messages.substr(11, 6);
console.log(res); // Output String

let res1 = messages.substr(11);
console.log(res1); // Output String substr

let res2 = messages.substr(-6, 6);
console.log(res2); // Output substr



